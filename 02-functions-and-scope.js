// Simple functions and scope

// Function to greet a user
function greet(name) {
    return "Hello, " + name + "!";
}

// Function to add two numbers
function add(a, b) {
    return a + b;
}

// Using the functions
console.log(greet("Alok")); // Output: Hello, Alok!
console.log(add(10, 20));    // Output: 30
